void PollTouchScreen(void)
{
	  GUI_PID_STATE TS_State;
	  static TS_StateTypeDef prev_state;
	  TS_StateTypeDef  ts;
	  uint16_t xDiff, yDiff;

	  BSP_TS_GetState(&ts);
	  if(ts.touchDetected > 0)
	  	ts.touchDetected = 1; // Get only one touch event in
	  TS_State.Pressed = ts.touchDetected;

	  xDiff = (prev_state.touchX[0] > ts.touchX[0]) ? (prev_state.touchX[0] - ts.touchX[0]) : (ts.touchX[0] - prev_state.touchX[0]);
	  yDiff = (prev_state.touchY[0] > ts.touchY[0]) ? (prev_state.touchY[0] - ts.touchY[0]) : (ts.touchY[0] - prev_state.touchY[0]);


	  if((prev_state.touchDetected != ts.touchDetected ) && ((xDiff > 3 ) || (yDiff > 3)))
	  {
	  	if(ts.touchDetected > 0)
	  		ts.touchDetected = 1;
	  	TS_State.Layer = 0;
	  	TS_State.x = ts.touchX[0];
	  	TS_State.y = ts.touchY[0];
	  	//GUI_DispDecAt(TS_State.x, 360, 10, 5);
	  	//GUI_DispDecAt(TS_State.y, 360, 30, 5);
	  	//GUI_DispDecAt(++t_counter, 360, 50, 5);
	  	GUI_TOUCH_StoreStateEx(&TS_State);
	  } else {
	  	ts.touchDetected = 0;
	  	TS_State.Layer = 0;
	  	TS_State.x = ts.touchX[0];
	  	TS_State.y = ts.touchY[0];
		GUI_TOUCH_StoreStateEx(&TS_State);
	  }
	  prev_state = ts;

	}
/* USER CODE END 4 */

